package CollegeDataObjects;

public class CostOfAttendance {
	private String books;
	private String roomAndBoard;
	private String personal;
	private String transportation;
	private String residentTuition;
	private String residentTotalCost;
	private String nonResidentTuition;
	private String nonResidentTotalCost;
	
	public CostOfAttendance() {
		
	}

	public String getBooks() {
		return books;
	}

	public void setBooks(String books) {
		this.books = books;
	}

	public String getRoomAndBoard() {
		return roomAndBoard;
	}

	public void setRoomAndBoard(String roomAndBoard) {
		this.roomAndBoard = roomAndBoard;
	}

	public String getPersonal() {
		return personal;
	}

	public void setPersonal(String personal) {
		this.personal = personal;
	}

	public String getTransportation() {
		return transportation;
	}

	public void setTransportation(String transportation) {
		this.transportation = transportation;
	}

	public String getResidentTuition() {
		return residentTuition;
	}

	public void setResidentTuition(String residentTuition) {
		this.residentTuition = residentTuition;
	}

	public String getResidentTotalCost() {
		return residentTotalCost;
	}

	public void setResidentTotalCost(String residentTotalCost) {
		this.residentTotalCost = residentTotalCost;
	}

	public String getNonResidentTuition() {
		return nonResidentTuition;
	}

	public void setNonResidentTuition(String nonResidentTuition) {
		this.nonResidentTuition = nonResidentTuition;
	}

	public String getNonResidentTotalCost() {
		return nonResidentTotalCost;
	}

	public void setNonResidentTotalCost(String nonResidentTotalCost) {
		this.nonResidentTotalCost = nonResidentTotalCost;
	}
	
	
}
