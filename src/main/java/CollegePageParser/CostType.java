package CollegePageParser;

public class CostType {
	private String livesWithParents;
	private String traditional;
	private String notTraditional;
	
	public CostType() {
		
	}

	public String getLivesWithParents() {
		return livesWithParents;
	}

	public void setLivesWithParents(String livesWithParents) {
		this.livesWithParents = livesWithParents;
	}

	public String getTraditional() {
		return traditional;
	}

	public void setTraditional(String traditional) {
		this.traditional = traditional;
	}

	public String getNotTraditional() {
		return notTraditional;
	}

	public void setNotTraditional(String notTraditional) {
		this.notTraditional = notTraditional;
	}
	

}
